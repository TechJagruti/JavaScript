        let a = 10;
        let b = 5;

        console.log("Values:");
        console.log("a = " + a);
        console.log("b = " + b);

        // AND (&&)
        console.log("(a > 5 && b < 10) : " + (a > 5 && b < 10));
        console.log("(a < 5 && b < 10) : " + (a < 5 && b < 10));

        // OR (||)
        console.log("(a < 5 || b < 10) : " + (a < 5 || b < 10));
        console.log("(a < 5 || b > 10) : " + (a < 5 || b > 10));

        // NOT (!)
        console.log("!(a > b) : " + !(a > b));
        console.log("!(a < b) : " + !(a < b));
