let x = 5;

console.log("Initial Value:");
console.log("x = " + x);

// Post-Increment
console.log("Post-Increment (x++) → " + (x++));
console.log("Value of x after x++ → " + x);

// Pre-Increment
console.log("Pre-Increment (++x) → " + (++x));
console.log("Value of x after ++x → " + x);

// Post-Decrement
console.log("Post-Decrement (x--) → " + (x--));
console.log("Value of x after x-- → " + x);

// Pre-Decrement
console.log("Pre-Decrement (--x) → " + (--x));
console.log("Value of x after --x → " + x);
